export type TranslationKey = keyof typeof dictionary.en;

export function t(
  language: LanguageCode | undefined,
  key: TranslationKey,
  params: Record<string, string | number> = {},
): string {
  const template = dictionary[language ?? 'en']?.[key] ?? dictionary.en[key] ?? key;
  return Object.entries(params).reduce((text, [paramKey, value]) => text.replaceAll(`{${paramKey}}`, String(value)), template);
}

export function languageInstruction(language: LanguageCode | undefined): string {
  return language === 'zh'
    ? 'Respond in Simplified Chinese. Keep JSON keys in English, but all user-facing text values must be Chinese.'
    : 'Respond in English. Keep JSON keys in English.';
}
